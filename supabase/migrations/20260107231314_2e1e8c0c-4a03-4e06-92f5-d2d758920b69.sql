-- Create enum types for statuses and roles
CREATE TYPE public.app_role AS ENUM ('host', 'guest', 'editor');
CREATE TYPE public.collaboration_status AS ENUM (
  'invited',
  'intake_completed',
  'scheduled',
  'recorded',
  'editing',
  'ready',
  'completed',
  'cancelled'
);
CREATE TYPE public.workspace_status AS ENUM ('active', 'archived');

-- Create profiles table for user data
CREATE TABLE public.profiles (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL UNIQUE REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name TEXT,
  avatar_url TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Create user_roles table (separate from profiles for security)
CREATE TABLE public.user_roles (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  role app_role NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (user_id, role)
);

-- Create workspaces table
CREATE TABLE public.workspaces (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  owner_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  status workspace_status NOT NULL DEFAULT 'active',
  welcome_message TEXT,
  welcome_video_url TEXT,
  max_reschedules INTEGER DEFAULT 2,
  reschedule_cutoff_hours INTEGER DEFAULT 24,
  external_storage_url TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Create guest_profiles table (reusable across collaborations)
CREATE TABLE public.guest_profiles (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  email TEXT NOT NULL,
  name TEXT NOT NULL,
  bio TEXT,
  headshot_url TEXT,
  website_url TEXT,
  social_links JSONB DEFAULT '[]'::jsonb,
  topics TEXT[],
  pronunciation_notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Create collaborations table
CREATE TABLE public.collaborations (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  workspace_id UUID NOT NULL REFERENCES public.workspaces(id) ON DELETE CASCADE,
  host_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  guest_profile_id UUID REFERENCES public.guest_profiles(id) ON DELETE SET NULL,
  editor_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  status collaboration_status NOT NULL DEFAULT 'invited',
  invite_token TEXT UNIQUE DEFAULT encode(gen_random_bytes(32), 'hex'),
  scheduled_date TIMESTAMPTZ,
  prep_date TIMESTAMPTZ,
  recorded_date TIMESTAMPTZ,
  editing_deadline TIMESTAMPTZ,
  delivery_date TIMESTAMPTZ,
  file_references JSONB DEFAULT '[]'::jsonb,
  posting_checklist JSONB DEFAULT '[]'::jsonb,
  reschedule_count INTEGER DEFAULT 0,
  notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Create tasks table (auto-generated by status)
CREATE TABLE public.tasks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  collaboration_id UUID NOT NULL REFERENCES public.collaborations(id) ON DELETE CASCADE,
  assigned_role app_role NOT NULL,
  assigned_user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  title TEXT NOT NULL,
  description TEXT,
  is_completed BOOLEAN DEFAULT false,
  is_skipped BOOLEAN DEFAULT false,
  due_date TIMESTAMPTZ,
  completed_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Create workspace_members table for editor assignments
CREATE TABLE public.workspace_members (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  workspace_id UUID NOT NULL REFERENCES public.workspaces(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  role app_role NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (workspace_id, user_id)
);

