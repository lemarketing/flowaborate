-- Create security definer function to check roles
CREATE OR REPLACE FUNCTION public.has_role(_user_id UUID, _role app_role)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.user_roles
    WHERE user_id = _user_id
      AND role = _role
  )
$$;

-- Create function to check workspace membership
CREATE OR REPLACE FUNCTION public.is_workspace_member(_user_id UUID, _workspace_id UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.workspaces WHERE id = _workspace_id AND owner_id = _user_id
    UNION
    SELECT 1 FROM public.workspace_members WHERE workspace_id = _workspace_id AND user_id = _user_id
  )
$$;

-- Create function to check collaboration access
CREATE OR REPLACE FUNCTION public.can_access_collaboration(_user_id UUID, _collaboration_id UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.collaborations c
    WHERE c.id = _collaboration_id
    AND (
      c.host_id = _user_id
      OR c.editor_id = _user_id
      OR EXISTS (
        SELECT 1 FROM public.guest_profiles gp 
        WHERE gp.id = c.guest_profile_id AND gp.user_id = _user_id
      )
    )
  )
$$;

-- Profiles policies
CREATE POLICY "Users can view their own profile"
  ON public.profiles FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can update their own profile"
  ON public.profiles FOR UPDATE
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own profile"
  ON public.profiles FOR INSERT
  WITH CHECK (auth.uid() = user_id);

-- User roles policies (read-only for users, managed by triggers)
CREATE POLICY "Users can view their own roles"
  ON public.user_roles FOR SELECT
  USING (auth.uid() = user_id);

-- Workspaces policies
CREATE POLICY "Owners can view their workspaces"
  ON public.workspaces FOR SELECT
  USING (owner_id = auth.uid() OR public.is_workspace_member(auth.uid(), id));

CREATE POLICY "Users can create workspaces"
  ON public.workspaces FOR INSERT
  WITH CHECK (owner_id = auth.uid());

CREATE POLICY "Owners can update their workspaces"
  ON public.workspaces FOR UPDATE
  USING (owner_id = auth.uid());

CREATE POLICY "Owners can delete their workspaces"
  ON public.workspaces FOR DELETE
  USING (owner_id = auth.uid());

-- Guest profiles policies
CREATE POLICY "Guests can view their own profile"
  ON public.guest_profiles FOR SELECT
  USING (
    user_id = auth.uid() 
    OR EXISTS (
      SELECT 1 FROM public.collaborations c
      WHERE c.guest_profile_id = guest_profiles.id
      AND (c.host_id = auth.uid() OR c.editor_id = auth.uid())
    )
  );

CREATE POLICY "Guests can update their own profile"
  ON public.guest_profiles FOR UPDATE
  USING (user_id = auth.uid());

CREATE POLICY "Users can create guest profiles"
  ON public.guest_profiles FOR INSERT
  WITH CHECK (true);

-- Collaborations policies
CREATE POLICY "Users can view collaborations they are part of"
  ON public.collaborations FOR SELECT
  USING (public.can_access_collaboration(auth.uid(), id));

CREATE POLICY "Hosts can create collaborations"
  ON public.collaborations FOR INSERT
  WITH CHECK (host_id = auth.uid());

CREATE POLICY "Hosts can update collaborations"
  ON public.collaborations FOR UPDATE
  USING (host_id = auth.uid() OR editor_id = auth.uid());

CREATE POLICY "Hosts can delete collaborations"
  ON public.collaborations FOR DELETE
  USING (host_id = auth.uid());

-- Allow public access to collaborations by invite token (for guest signup)
CREATE POLICY "Anyone can view collaboration by invite token"
  ON public.collaborations FOR SELECT
  USING (invite_token IS NOT NULL);

-- Tasks policies
CREATE POLICY "Users can view tasks for their collaborations"
  ON public.tasks FOR SELECT
  USING (public.can_access_collaboration(auth.uid(), collaboration_id));

CREATE POLICY "Assigned users can update their tasks"
  ON public.tasks FOR UPDATE
  USING (
    assigned_user_id = auth.uid()
    OR EXISTS (
      SELECT 1 FROM public.collaborations c
      WHERE c.id = collaboration_id AND c.host_id = auth.uid()
    )
  );

CREATE POLICY "System can create tasks"
  ON public.tasks FOR INSERT
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.collaborations c
      WHERE c.id = collaboration_id AND c.host_id = auth.uid()
    )
  );

-- Workspace members policies
CREATE POLICY "Workspace owners can manage members"
  ON public.workspace_members FOR ALL
  USING (
    EXISTS (
      SELECT 1 FROM public.workspaces w
      WHERE w.id = workspace_id AND w.owner_id = auth.uid()
    )
  );

CREATE POLICY "Members can view their membership"
  ON public.workspace_members FOR SELECT
  USING (user_id = auth.uid());

-- Create trigger for auto-updating timestamps
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SET search_path = public;

CREATE TRIGGER update_profiles_updated_at
  BEFORE UPDATE ON public.profiles
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_workspaces_updated_at
  BEFORE UPDATE ON public.workspaces
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_guest_profiles_updated_at
  BEFORE UPDATE ON public.guest_profiles
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_collaborations_updated_at
  BEFORE UPDATE ON public.collaborations
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_tasks_updated_at
  BEFORE UPDATE ON public.tasks
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- Create trigger to auto-create profile and assign host role on user signup
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Create profile
  INSERT INTO public.profiles (user_id, full_name)
  VALUES (NEW.id, NEW.raw_user_meta_data ->> 'full_name');
  
  -- Assign host role by default (users can be hosts)
  INSERT INTO public.user_roles (user_id, role)
  VALUES (NEW.id, 'host');
  
  RETURN NEW;
END;
$$;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

